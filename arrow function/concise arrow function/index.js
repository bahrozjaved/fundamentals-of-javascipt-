// Concise arrow function are mostly the one liner function in ES6 that will be used 
const multiply = (a, b) => a * b;
console.log(multiply(2, 30));

const add = (a, b) => a + b;
console.log(add(2, 30));

const subtract = (a, b) => a - b;
console.log(subtract(2, 30));

const divide = (a, b) => a / b;
console.log(divide(2, 30));

const percentage = (a, b) => (a / b) * 100;
console.log(percentage(28, 30));

const remainder =(a,b) => a%b;
console.log(remainder(4,4))